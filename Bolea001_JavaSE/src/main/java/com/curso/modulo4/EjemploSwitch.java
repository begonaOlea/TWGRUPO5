package com.curso.modulo4;

public class EjemploSwitch {

	public static void main(String[] args) {
		
		int a = 1;
		
		switch (a) {
		default:
			System.out.println("default");
		case 1:
			System.out.println("uno");
			//break;
		case 2:
			System.out.println("dos");
		    break;
		}
		
	}
	
	
}
